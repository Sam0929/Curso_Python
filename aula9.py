#Operador aritméticos


adicao = 10 + 10
print('Adição:', adicao)

subtracao = 10 - 10
print('Subtração:', subtracao)

multiplicacao = 10 * 10
print('Multiplicação:', multiplicacao)

divisao = 10 / 2.2  # float
print('Divisão:', divisao)

divisao_inteira = 10 // 2.2  #retorna inteiro se os dois forem inteiros, caso contrario, retorna um float porém sem as casas decimais
print('Divisão Inteira:', divisao_inteira) 

exponenciacao = 2 ** 10
print('Exponenciação', exponenciacao)

modulo = 55 % 2 #  resto
print('Módulo', modulo)

print (exponenciacao % 2 == 0)

