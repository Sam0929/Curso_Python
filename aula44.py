"""
For + Range

range -> range (start, stop, step)

"""


numbers = range (0, 10) # começa em 0 e termina em 10
number1 = range(5, 10) # Começa em 5 e termina em 10
number2 = range(5, 10, 2) # Começa em 5 e vai de 2 em 2 até 10

print(numbers)

print(number1)

print(number2)

for number in numbers:
    print(number)

